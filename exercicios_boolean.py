entrada2 = input('Digite "Verdadeiro" ou "Falso": ')

valor1 = entrada1.upper().strip() == "VERDADEIRO"
valor2 = entrada2.upper().strip() == "VERDADEIRO"

resultado = valor1 and valor2

print(resultado)

